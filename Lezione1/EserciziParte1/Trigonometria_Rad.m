%% ES.4 Si calcolino i risultati per almeno 2 angoli noti delle seguenti
% funzioni trigonometriche: cos, sin, acos, asin, tan, atan, cot, acot

Angle_1 = pi;
Angle_2 = 2 * pi; %angoli in radianti

%% COSENO
fprintf(" cos(%4.4f) = %2.4f \n", Angle_1, cos(Angle_1));
fprintf(" cos(%4.4f) = %2.4f \n", Angle_2, cos(Angle_2));

%% SENO
fprintf(" sin(%4.4f) = %2.4f \n", Angle_1, sin(Angle_1));
fprintf(" sin(%4.4f) = %2.4f \n", Angle_2, sin(Angle_2));

%% ARCOSENO
fprintf(" acos(%4.4f) = %2.4f \n", Angle_1, acos(Angle_1));
fprintf(" acos(%4.4f) = %2.4f \n", Angle_2, acos(Angle_2));

%% ASENO
fprintf(" asin(%4.4f) = %2.4f \n", Angle_1, asin(Angle_1));
fprintf(" asin(%4.4f) = %2.4f \n", Angle_2, asin(Angle_2));

%% TANGENTE
fprintf(" tan(%4.4f) = %2.4f \n", Angle_1, tan(Angle_1));
fprintf(" tan(%4.4f) = %2.4f \n", Angle_2, tan(Angle_2));

%% ARCOTANGENTE
fprintf(" atan(%4.4f) = %2.4f \n", Angle_1, atan(Angle_1));
fprintf(" atan(%4.4f) = %2.4f \n", Angle_2, atan(Angle_2));

%% COTANGENTE
fprintf(" cotan(%4.4f) = %2.4f \n", Angle_1, cot(Angle_1));
fprintf(" cotan(%4.4f) = %2.4f \n", Angle_2, cot(Angle_2));

%% ARCOCOTANGETE
fprintf(" acotan(%4.4f) = %2.4f \n", Angle_1, acot(Angle_1));
fprintf(" acotan(%4.4f) = %2.4f \n", Angle_2, acot(Angle_2));

